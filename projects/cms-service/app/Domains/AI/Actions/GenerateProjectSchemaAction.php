  }

  // ─────────────────────────────────────────────────────────
  private function buildPromptWithHistory(
    string $currentPrompt,
    array  $history
  ): string {
    if (empty($history)) {
      return $currentPrompt;
    }

    $context  = "=== CONVERSATION HISTORY ===\n\n";

    foreach ($history as $message) {
      $role     = $message['role'] === 'user' ? 'USER' : 'ASSISTANT';
      $context .= "[{$role}]:\n{$message['content']}\n\n";

      if ($message['role'] === 'assistant' && !empty($message['schema'])) {
        $context .= "[PREVIOUS SCHEMA]:\n";
        $context .= json_encode($message['schema'], JSON_UNESCAPED_UNICODE | JSON_PRETTY_PRINT);
        $context .= "\n\n";
      }
    }

    $context .= "=== CURRENT REQUEST ===\n{$currentPrompt}\n\n";
    $context .= "IMPORTANT: Return the COMPLETE updated schema with all changes applied.";

    return $context;
  }

  // ─────────────────────────────────────────────────────────
  private function parseJson(string $text): array
  {
    $text  = str_replace(['```json', '```'], '', $text);
    $text  = trim($text);
    $start = strpos($text, '{');
    $end   = strrpos($text, '}');

    if ($start !== false && $end !== false) {
      $text = substr($text, $start, $end - $start + 1);
    }

    $decoded = json_decode($text, true, 512, JSON_UNESCAPED_UNICODE);
    return is_array($decoded) ? $decoded : [];
  }
}
