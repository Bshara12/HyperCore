<?php

namespace App\Domains\AI\Actions;

use App\Domains\AI\Providers\AIProviderChain;
use Illuminate\Support\Facades\Log;

class GenerateProjectSchemaAction
{
  public function __construct(
    private AIProviderChain $chain
  ) {}

  public function execute(string $userPrompt, array $conversationHistory = []): array
  {
    $systemPrompt = $this->buildSystemPrompt();
    $fullPrompt   = $this->buildPromptWithHistory($userPrompt, $conversationHistory);

    try {
      // ✅ الـ chain يجرب كل provider تلقائياً
      $result  = $this->chain->generate($systemPrompt, $fullPrompt);
      $rawText = $result['text'];

      Log::info("[AI Schema] Generated by: {$result['provider']}");

      // لو في providers فشلت قبل النجاح — سجّل تحذير
      if (!empty($result['errors'])) {
        Log::warning("[AI Schema] Fallback used. Failed providers: " .
          collect($result['errors'])->keys()->implode(', '));
      }

      $parsedData = $this->parseJson($rawText);

      return [
        'success'          => true,
        'ai_chat_bubble'   => $parsedData['assistant_message'] ?? 'لم يتم استلام رسالة.',
        'technical_schema' => $parsedData['schema']            ?? [],
        'provider_used'    => $result['provider'],
      ];
    } catch (\Exception $e) {
      Log::error("[AI Schema] All providers failed: " . $e->getMessage());
      throw $e;
    }
  }

  // ─────────────────────────────────────────────────────────
  private function buildSystemPrompt(): string
  {
    return <<<EOT
Role: You are HyperCore AI, a Senior System Architect for HyperCore CMS — 
a modular Laravel-based platform. Transform user business ideas into a 
production-ready technical schema IMMEDIATELY.

════════════════════════════════════════
1. THE "NO-QUESTIONS" MANDATE (CRITICAL)
════════════════════════════════════════
- NEVER ask for clarifications, project names, or languages.
- ACT IMMEDIATELY using logical assumptions.
- DEFAULT LANGUAGES: ["ar", "en"] unless specified.
- DEFAULT PROJECT NAME: Generate a creative professional name.

════════════════════════════════════════
2. INTELLIGENT MODULE ACTIVATION
════════════════════════════════════════
Activate modules based on business model:
- cms:       ALWAYS active
- ecommerce: sell, buy, store, products, cart, payment, orders
- booking:   appointment, rent, schedule, reserve, slot, availability

════════════════════════════════════════
3. BUILT-IN vs CUSTOM PROTOCOL
════════════════════════════════════════
NATIVE (DO NOT recreate):
- ecommerce: Cart, Orders, Payments, Offers, Wishlist, Returns
- booking:   Resources, Bookings, Availabilities, Policies
- cms:       Projects, DataTypes, DataEntries, Collections, SEO, Ratings

CUSTOM: Only unique business entities.

════════════════════════════════════════
